        <p>　　从这一刻开始，我的人生已经改写，一个已经被毁灭了的人的人生，注定不会是一个好的开始……</p>
        <p>　　我一直在想，以什么样的开头来讲述这个故事。</p>
        <p>　　彼时西雅图的夜色正浓，灯火港湾正在山脚下闪烁，透过窗户望出去，除了墨黑的天幕，只能看见密密的树林，从树林的那边倒是隐约透出橘色的光晕来。我知道，那是湖区两岸密密匝匝的灯海过于璀璨的缘故，于是衬得天上的星光也暗淡了。</p>
        <p>　　西雅图不眠夜，正是由此而来。</p>
        <p>　　我闭上眼睛，许多往事在眼前一幕一幕，不知怎么变得模糊不清。很多的面孔在脑海中浮现，活着的，死去的，爱着的，恨过的……太多太多的面孔沉下去，又浮上来，“他们”隔着时空斑驳的光影默默注视着我，目光悲凉如窗外的夜风。</p>
        <p>　　很多人总是在认识后才知道不该认识，很多事情总是在发生过后才知道错了，很多时候总是明知道错了还要继续错下去。</p>
        <p>　　可是，我依然无悔。</p>
        <p>　　时光倒回到五年前的7月13日，那天其实是个很平常的日子，可越是平常越有发生不平常事情的可能，一点儿征兆都不会给你。那天我在做什么？我在星城的一家西餐厅和米兰、李樱之两个老同学在享受购物后的美味大餐，三个人吃吃喝喝，有说有笑，热闹得不行。</p>
        <p>　　那家餐厅的环境很幽雅，空气中弥漫着牛排、咖啡、红酒和各种香水的味道，混浊不清，感觉灯光都有点蛊惑人心。一个面容清秀的女孩坐在一架黑色钢琴前专注地演奏，曲子很熟悉，是卡朋特的《昨日重现》，弹得还不赖，有那么一点怀旧的味道。我那天的谈兴很好，讲起了大学时跟教授作对的种种趣事，把米兰和樱之逗得哈哈大笑，但当这首曲子一响起，我感觉身体内的某根神经隐隐地抽搐了一下，很轻微，还没感觉到痛就消失了。</p>
        <p>　　如果不是后来发生的事，我根本就不会想起这次似是而非的触动，如果一定要说预感，这也许是那天我唯一感觉到的异样，只是当时我并没意识到这点，愣了一会儿神，又恢复了谈笑风生，全然不知在毗邻的另一座城市灾难正悄然降临——</p>
        <p>　　只是几秒钟！我的丈夫祁树杰驾着一辆白色本田义无反顾地冲入湖中，那么决然，那么悲怆，没有任何的犹豫，好像那是一件必然要做的事情，任谁都不能阻止。这真是例外啊，他这人平时做事就喜欢拖拖拉拉，有时候决定了的事，一遇到情况，马上又变卦。他好像从来没有很坚决地要去做过一件什么事，他整个人生都是犹犹豫豫的，如果硬要比较，那就只有两次还算是比较坚决的，一次就是四年前坚决地娶了我，一次就是四年后的今天坚决地去死。</p>
        <p>　　关于他的死，后来传出很多版本，有人说是被人劫持谋财害命，有人说是欠了债想不开寻了短见，还有人说是喝醉了酒发酒疯一不小心冲进湖中……反正说什么的都有，每天都有新的说法传出来，祁树杰在那些人的唾沫中不知道“死”了多少回。这恐怕也是他没想到的，他这人一向低调，最不喜欢被人说三道四，也不喜欢处在风口浪尖，只要有选择，他永远都选择退居幕后。真没想到他这么低调的一个人，却死得这么轰轰烈烈，连从小出风头出惯了的我都望尘莫及。而有关他死时的真实情况，却是后来警方提供的。</p>
        <p>　　据他们调查，那辆白色本田在湖边的树荫下停了整整一个下午，纹丝不动，不知怎么到了傍晚，路灯已经亮起，人们到湖边散步纳凉时，车子突然像暴怒的狮子般咆哮着开足马力飞腾而起，在空中划了条优美的弧线后，一声闷响扎进了湖中。那个画面一定很壮观，就像很多汽车广告，疾速飞驰，追风赶月，行云流水般尽显完美，我每次在电视里看到那样的汽车广告，就会想象一次祁树杰死时的情景，所以祁树杰在我的想象里也不知道“死”了多少回。</p>
        <p>　　扯远了，还是回到事发的当天。车子冲入湖中后立即引来一阵惊叫，围观的人从四面八方赶来，救护车和警车也先后赶到。但都一筹莫展，因为车已沉入湖底，湖面一片宁静。湖水依然荡漾着迷人的波浪，夜风习习，繁星点点，仿佛什么也未曾发生过一样。</p>
        <p>　　接着警察开始封锁现场，一辆吊车开了进来，几个潜水员潜入湖中实施打捞。岸边一时间灯火通明，人声鼎沸。凌晨四点左右，冲入湖中的本田终于浮出水面。吊车小心地将其吊向岸边，车门打开，里面的人被抬了出来，祁树杰和一个女人湿漉漉的紧紧地抱在一起。一个女人！没错，是一个女人！</p>
        <p>　　全城轰动。</p>
        <p>　　所有的人都在议论。</p>
        <p>　　一男一女驾车驶入美丽的南湖，两人被捞上来时还手指扣着手指。现场留有一封遗书，用塑料胶纸密封好了的，显然死者生前经过精心准备。那封遗书只有一句话：对不起所有的人，但别无选择，因为我们已生无可恋……</p>
        <p>　　去他妈的生无可恋！我的愤怒一度盖过了失去丈夫的悲痛！什么叫生无可恋？他怎么就生无可恋了？有房有车有公司，外有朋友家有慈母枕边有漂亮娇妻，不愁吃不愁穿身体健康前景光明，唯一小小的烦恼不过是婆媳关系有点紧张，可这就让他去寻死吗？该去寻死的是我，是我！每次被他老妈指着骂时，我都气得想死，可是我不也没死，一直 撑到现在吗？</p>
        <p>　　“我早晚会死给你看！”每次在老太婆面前受了气我都这么冲他吼。</p>
        <p>　　可是老天，我还没死，他却先死了，平时做什么事总是他落在后面，怎么这一次就让他抢了先呢？到底是哪根神经搭错了，最后竟成了他死给我看？</p>
        <p>　　我绞尽脑汁也想不明白，我“美满”的婚姻怎么走到了这个地步，现在哪怕是坐在机场，事情已经过去几个月了，我还是想不通一向言听计从的祁树杰怎么敢跟我开这么天大的玩笑。我一直当他是在开玩笑，明知道是自欺欺人也深信不疑。</p>
        <p>　　也只有在这个时候，我才对我一向看不起的丈夫“刮目相看”，二十六岁就让我成了一个寡妇，这浑蛋出手比我狠多了，让我连质问的机会都没有！你说他狠不狠？</p>
